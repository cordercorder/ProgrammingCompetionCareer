const int maxn=2010;
const int inf=(int)1e6;

int n,k,ans;
char s[maxn+maxn][maxn+maxn];
int sumr[maxn+maxn][maxn+maxn],sumc[maxn+maxn][maxn+maxn];
int revr[maxn][maxn],revc[maxn][maxn];

void solve(){
    int lim=n+k-1,tmp=0,maxe=0;
    for(int i=1;i<=lim;i++){
        for(int j=1;j<=lim;j++){
            sumr[i][j]=sumr[i-1][j];
            sumc[i][j]=sumc[i][j-1];
            if(s[i][j]=='B'){
                sumr[i][j]++;
                sumc[i][j]++;
            }
        }
    }
    for(int i=1;i<=n;i++){
        if(sumr[n][i]==0){
            ans++;
        }
        if(sumc[i][n]==0){
            ans++;
        }
    }
    for(int i=1;i<=n;i++){
        for(int j=1;j<=n;j++){
            revc[i][j]=revc[i][j-1];
            if(sumr[i+k-1][j]-sumr[i-1][j]==sumr[n][j]&&sumr[n][j]>0){
                revc[i][j]++;
            }
            revr[i][j]=revr[i-1][j];
            if(sumc[i][j+k-1]-sumc[i][j-1]==sumc[i][n]&&sumc[i][n]>0){
                revr[i][j]++;
            }
        }
    }
    lim=n-k+1;
    for(int i=1;i<=lim;i++){
        for(int j=1;j<=lim;j++){
            tmp=revc[i][j+k-1]-revc[i][j-1]+revr[i+k-1][j]-revr[i-1][j];
            maxe=max(maxe,tmp);
        }
    }
    ans+=maxe;
    printf("%d\n",ans);
}

int main(void){
    scanf("%d %d",&n,&k);
    memset(s,'\0',sizeof(s));
    for(int i=1;i<=n;i++){
        scanf("%s",s[i]+1);
    }
    solve();
    return 0;
}
